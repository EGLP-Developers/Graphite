package me.eglp.gv2.main;

public class GraphiteSetupException extends RuntimeException {

	private static final long serialVersionUID = -8917004305052103858L;

	public GraphiteSetupException() {
		super();
	}

	public GraphiteSetupException(String message, Throwable cause) {
		super(message, cause);
	}

	public GraphiteSetupException(String message) {
		super(message);
	}

	public GraphiteSetupException(Throwable cause) {
		super(cause);
	}
	
}
